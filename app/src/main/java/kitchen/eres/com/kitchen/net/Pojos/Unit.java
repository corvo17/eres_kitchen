package kitchen.eres.com.kitchen.net.Pojos;

public class Unit {
    private long id;
    private String name;
    private String name_ru;
    private String name_uzl;
    private String name_uzc;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName_ru() {
        return name_ru;
    }

    public void setName_ru(String name_ru) {
        this.name_ru = name_ru;
    }

    public String getName_uzl() {
        return name_uzl;
    }

    public void setName_uzl(String name_uzl) {
        this.name_uzl = name_uzl;
    }

    public String getName_uzc() {
        return name_uzc;
    }

    public void setName_uzc(String name_uzc) {
        this.name_uzc = name_uzc;
    }
}
